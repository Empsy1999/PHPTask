using System;

class Program
{
    static void Main()
    {
        // Example usage of the function
        int result = Sum(5, 10);
        Console.WriteLine("Sum of 5 and 10 is: " + result);
    }

    // Function to calculate the sum of two integers
    static int Sum(int a, int b)
    {
        return a + b;
    }
}

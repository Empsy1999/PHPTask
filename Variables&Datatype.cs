using System;

class Program
{
    static void Main()
    {
        // Declaring and initializing variables
        int integerVar = 10;
        float floatVar = 3.14f;
        double doubleVar = 2.71828;
        char charVar = 'A';
        string stringVar = "Hello, C#!";
        bool boolVar = true;

        // Outputting the values
        Console.WriteLine("Integer Variable: " + integerVar);
        Console.WriteLine("Float Variable: " + floatVar);
        Console.WriteLine("Double Variable: " + doubleVar);
        Console.WriteLine("Character Variable: " + charVar);
        Console.WriteLine("String Variable: " + stringVar);
        Console.WriteLine("Boolean Variable: " + boolVar);
    }
}
